#include "libft.h"

size_t	ft_power(int base, int pwr)
{
	size_t	res;

	res = 1;
	if (pwr < 0)
		return (0);
	while (pwr > 0)
	{
		res *= base;
		pwr--;
	}
	return (res);
}
